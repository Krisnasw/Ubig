								<p>From the engine, power goes through Chrysler's all-new dual-motor electronically variable transmission. The two electric motors in the transmission are mated to the engine via a planetary gear set. "In addition," he said, "we added a one-way clutch to that gear set so that now we can take what's normally a generator-only motor and make that do double duty and have it go back and be a generator when it needs to be one and be a drive motor when we want it to be one so that we can actually drive the vehicle with two electric motors. Now we can play the best efficiency game between the two motors. We can be very efficient in electric mode and certainly not be handcuffed like you would if you couldn't switch back and forth. Plus we don't lose any regen capability because as soon as we have the opportunity to regen, we can swap that back and get it back into regen mode and off we go."</p>
