								<p>Under the hood (and the floorboards, where the batteries are) there are a lot of new bits. Mets said that the transmission is all new and the engine is the new 3.6-liter Pentastar engine without cooled exhaust gas recirculation and the two-step valve lift. The Pacifica's engine also uses the Atkinson cycle, which is a good fit for hybrid vehicles.</p>
								<p>"Basically what you do is you leave your intake valve open during the combustion," Mets said, describing Atkinson cycle. "You'll lose a little bit of power when you do that but you also lose a lot of the pumping losses that you would have. The vehicle is more efficient. We can afford to lose a little bit of internal combustion power because we have the 16-kilowatt hour battery that can provide all the power you need instantaneously. You don't have to worry about the horsepower aspect of it. Basically you want to tailor that engine to be more efficient as a generator than anything else."</p>
